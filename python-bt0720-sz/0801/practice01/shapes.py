### 정사각형과 원의 넓이를 계산하는 함수 모듈 ###

# 1. calculate_square_area 함수 (정사각형의 넓이)
 # 한 변의 길이를 인자로 받아 정사각형의 넓이를 반환

def calculate_square_area(side) :
    return side * side

# 2. calculate_circle_area 함수 (원의 넓이)
 # 반지름을 인자로 받아 원의 넓이를 반환
 # 반지름 * 반지름 * 3.14

def calculat_circle_area(r) :
    return r * r * 3.14